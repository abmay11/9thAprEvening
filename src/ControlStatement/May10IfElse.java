package ControlStatement;

public class May10IfElse {

	public static void main(String[] args) 
	{
		        //eg.If my grade is A i got 90% else i got below 90%
				char grade='A';
				
				if(grade=='A') //if grade is other than A then else part will execute
				{
					System.out.println("i got above 90%");
				}
				
				else 
				{
				System.out.println("i got below 90%");
					
				}
	}

}
