package Loop;

public class Test {

	public static void main(String[] args) {
		
		int a=4;
		System.out.println(a);
		a++;//a=a+1
		System.out.println(a);
		a=a+2;//2 onward this syntax
		System.out.println(a);//here a will be 5+2=7..latest value
		
		a--;//a=a-1
		System.out.println(a);//a=7-1=6
		a=a-2;
		System.out.println(a);
	}

}
