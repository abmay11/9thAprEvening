package Loop;

public class May11AssignmentLoop {

	public static void main(String[] args) {
		
		//Ex.table of 2
		for(int a=2;a<=20;a=a+2) 
		{
		System.out.println(a);	
		}
		System.out.println("========");
		
		//Ex.reverse of table 2
		for(int a=20;a>=2;a=a-2) 
		{
			System.out.println(a);
		}
		

	}

}
